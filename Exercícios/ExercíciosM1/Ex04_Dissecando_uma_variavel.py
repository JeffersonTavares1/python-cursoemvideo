


a = input('Digite algo: ')

print('\nO tipo primitivo dese valor e: ', type(a))
print('So tem espaço? ', a.isspace())
print('E um número? ', a.isnumeric())
print('E alfabético? ', a.isalpha())
print('E alfanumérico ', a.isalnum())
print('Esta em maiusculas? ', a.isupper())
print('Esta em minúsculas? ', a.islower())

print('Esta capitalizado? ', a.istitle())











